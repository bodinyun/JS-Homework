// ให้เขียนโค้ดแสดงสูตรคูณตั้งแต่ แม่ 2 ถึง แม่ 12 โดยแสดงผลออกมาใน console  
// 2 x 1 = 2  
// 2 x 2 = 4  
// ...  
// หมายเหตุ : คำสั่งขึ้นบรรทัดใหม่ใน console ให้ใช้ \n

let sum;

for (i = 1; i < 13; i++) {

    for (j = 1; j < 13; j++) {

        sum = i * j;
        console.log(`(${i} x ${j} = ${sum})`)

    }

}


