// หมายเหตุ : คำสั่งขึ้นบรรทัดใหม่ใน console ให้ใช้ \n

let star = '';

for (i = 0 ; i < 10 ; i++) {

    for (j = 1; j < i ; j++) {

      star += j;
      star += ' '

    } star += "\n";

}

console.log(star);